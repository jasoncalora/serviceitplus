            var http = new XMLHttpRequest();
            var url = "sendmail2.php";
            //        var url = "inc/preview.php";
            http.open('POST', url, true);
            //Send the proper header information along with the request
            http.setRequestHeader('Content-type', 'application/x-www-form-urlencoded');

            http.onreadystatechange = function() { //Call a function when the state changes.
                if (http.readyState == 4 && http.status == 200) {
//                    alert(http.responseText);
                    document.getElementById("statusmessage").textContent = http.responseText;
                    document.getElementById("loader2").style.display = "none";
                    $('#contactmodal2').modal('hide');
                    $('#statusmodal').modal('show');
                }
            }
            http.send(params);
            document.getElementById("txt_fname").value = "";
            document.getElementById("txt_lname").value = "";
            document.getElementById("txt_company").value = "";
            document.getElementById("txt_email").value = "";
            document.getElementById("txt_contact").value = "";
