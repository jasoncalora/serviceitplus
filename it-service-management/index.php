            if (/^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/.test(email)) {
                document.getElementById("emailrequireds").innerHTML = "";
                document.getElementById("emailrequireds").style.opacity = 0;
            } else {
                document.getElementById("emailrequireds").innerHTML = "*Invalid Email Address";
                document.getElementById("emailrequireds").style.color = "red";
                document.getElementById("emailrequireds").style.opacity = 1;
                x += 1;
            }
        }
        if (contact == "") {
            document.getElementById("contactrequireds").innerHTML = "*Required";
            document.getElementById("contactrequireds").style.color = "red";
            document.getElementById("contactrequireds").style.opacity = 1;
            x += 1;
        } else {
            document.getElementById("contactrequireds").innerHTML = "";
            document.getElementById("contactrequireds").style.opacity = 0;
        }
